% 定义 s 为传递函数变量
s = tf('s');

% --- 情况 1: K = 1 ---
K1 = 1;
G1 = (10 * K1 * (s + 0.5)) / (s^2 * (s + 2) * (s + 10));

% --- 情况 2: K = 10 ---
K2 = 10;
G2 = (10 * K2 * (s + 0.5)) / (s^2 * (s + 2) * (s + 10));

% --- 绘制奈氏图 ---
figure;
nyquist(G1, G2);
grid on;
title('5-10 (K=1 和 K=10)');
legend('K = 1', 'K = 10', 'Location', 'southeast');

% --- 放大关键区域 (包括原点和 (-1, 0) 点) ---
% 设置坐标轴范围: [xmin, xmax, ymin, ymax]
axis([-1.5, 0.5, -1, 1]);