% --- 方案一：使用 s = tf('s') ---

% 清理工作区和命令窗口
clear;
clc;

% 1. 定义 s 为传递函数变量
s = tf('s');

% 2. 定义开环传递函数 (不包含 K*)
% G(s) = (s+2) / (s(s+1)(s+3))
sys = (s + 2) / (s * (s + 1) * (s + 3));

% 3. 绘制根轨迹
figure;         % 创建一个新的图形窗口
rlocus(sys);    % 绘制根轨迹图

% 4. (可选) 添加标题和网格
title('G(s) = K(s+2) / (s(s+1)(s+3)) 的根轨迹');
grid on;        % 显示网格
sgrid;          % (可选) 添加阻尼比和自然频率的网格