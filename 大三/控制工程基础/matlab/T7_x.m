% 清除工作区
clear; clc;

% 定义传递函数变量 s
s = tf('s');

% 定义第一个系统 G(s)
% G(s) = 300 / (s * (0.03s + 1) * (0.047s + 1))
G = 300 / (s * (0.03*s + 1) * (0.047*s + 1));

% 定义第二个系统 G(s)Gc(s)
% G(s)Gc(s) = (300 * (0.5s + 1)) / (s * (10s + 1) * (0.03s + 1) * (0.047s + 1))
G_total = (300 * (0.5*s + 1)) / (s * (10*s + 1) * (0.03*s + 1) * (0.047*s + 1));

% 绘制伯德图对比
figure;
bode(G, G_total);

% 添加图例和网格
legend('未校正系统 G(s)', '校正后系统 G(s)Gc(s)');
grid on;
title('系统伯德图对比');